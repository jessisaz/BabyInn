const date = new Date();
const currentYear = new Date().getFullYear();
const currentMonth = new Date().getMonth();
const today = new Date().getDate();

var slecteddate;

const renderCalendar = (x) => {
  date.setDate(1);

  const monthDays = document.querySelector(".days");

  const lastDay = new Date(
    date.getFullYear(),
    date.getMonth() + 1,
    0
  ).getDate();

  const prevLastDay = new Date(
    date.getFullYear(),
    date.getMonth(),
    0
  ).getDate();

  const firstDayIndex = date.getDay();

  const lastDayIndex = new Date(
    date.getFullYear(),
    date.getMonth() + 1,
    0
  ).getDay();

  const nextDays = 7 - lastDayIndex - 1;

  const months = [
    "January",
    "February",
    "March",
    "April",
    "May",
    "June",
    "July",
    "August",
    "September",
    "October",
    "November",
    "December",
  ];

  document.querySelector(".month h1").innerHTML = months[date.getMonth()];
  document.querySelector(".month h3").innerHTML = date.getFullYear();

  let days = "";

  for (let x = firstDayIndex; x > 0; x--) {
    days += `<div class="prev-date">${prevLastDay - x + 1}</div>`;
  }

  for (let i = 1; i <= lastDay; i++) {
      if (i < today && date.getMonth() == currentMonth && date.getFullYear() == currentYear) {
        days += `<div class="prev-date">${i}</div>`
      } else if (date < new Date(currentYear, currentMonth-1, today)) {
        days += `<div class="prev-date">${i}</div>`
      } else if (i == x) {
        days += `<div class="today" onclick=clickdate(this)>${i}</div>`;
      } else {
        days += `<div onclick=clickdate(this)>${i}</div>`;
      }
  }

  for (let j = 1; j <= nextDays; j++) {
    days += `<div class="next-date">${j}</div>`;
    monthDays.innerHTML = days;
  }
};


document.querySelector(".prev_month").addEventListener("click", () => {
  date.setMonth(date.getMonth() - 1);
  renderCalendar();
});

document.querySelector(".next_month").addEventListener("click", () => {
  date.setMonth(date.getMonth() + 1);
  renderCalendar();
});


function clickdate(event) {
  
  if (slecteddate === `${event.innerHTML}/${date.getMonth() + 1}`) {
    event.style.backgroundColor = "#FFFFFF"
    return;
  }
  slecteddate = `${event.innerHTML}/${date.getMonth() + 1}`;
  console.log(slecteddate);
  
  renderCalendar(event.innerHTML)
}

renderCalendar();

